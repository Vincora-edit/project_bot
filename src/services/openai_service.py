            "–§–æ—Ä–º–∞—Ç –æ—Ç–≤–µ—Ç–∞:\n"
            "–î–†–£–ñ–ï–õ–Æ–ë–ù–´–ô:\n<—Ç–µ–∫—Å—Ç>\n\n"
