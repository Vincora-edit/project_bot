            "- –ë—ã—Ç—å –≥–æ—Ç–æ–≤—ã–º –∫ –æ—Ç–ø—Ä–∞–≤–∫–µ (–±–µ–∑ –ø—Ä–∞–≤–æ–∫)\n"
